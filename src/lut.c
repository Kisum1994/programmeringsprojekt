/* vi skal definere integer med 32 bit, da computeren ikke nødvendigvis definerer integers med 32 bits. Vi vil gerne være sikre på at det passer med funktionen, som printer værdierne*/
int32_t calc_sin(int32_t x){
    /* funktionen kaldes calc_sin og argumentet, som modtages i grader kaldes x */
int32_t input=(x*512)/360;
/* da sinus funktionen deler cirklen i 512 dele laves argumentet om til radianer på overstående måde */
return(int32_t)(sinus[input&0x1FF]);
/* her beder vi funktionen om at returnere et tal i 32 bit format, som skal tage værdien af Sinus i LUT af det input vi har givet */
/* udover dette benyttes den logisk og kommando, som sammenligner tallet med 512 som er det antal indelinger der er af vores cirkel.
Her benyttes den logiske og operator som vil gøre at tallet ikke kan være større end 512. Ellers vil det blive afrundet korrekt, grundet det binæere talsystem */
}

/* cosinus skrives analogt, blot forskudt med 90 grader*/
int32_t calc_cos(int32_t x){
    int32_t input=(x*521+90)/360;
    return(int32_t)(sinus[input&0x1FF]);
}
